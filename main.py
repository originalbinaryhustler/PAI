def change_ip(interface_name, new_ip, default_gateway, subnet_mask):
   if os.name == 'nt': # Windows
       command = f'netsh interface ip set address name="{interface_name}" static {new_ip} {default_gateway} {subnet_mask} '
   else: # Linux
       command = f'sudo ip addr add {new_ip}/{subnet_mask} dev {interface_name}; sudo route add default gw {default_gateway}'
   subprocess.run(command, shell=True)   
   

